SELECT 
    user_email as email,
    case when meta_value is null then 'free' else 'pro' end as access_level,
    meta_value as stripe_id
FROM 
        wp_users as u
    LEFT JOIN
        wp_usermeta as m
            ON u.ID = m.user_id
            AND m.meta_key = 'stripe_customer_id'

